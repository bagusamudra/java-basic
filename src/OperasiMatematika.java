public class OperasiMatematika {

    public static void main(String[] args) {

        int c = 100;

        c += 10;
        System.out.println(c);

        c -= 10;
        System.out.println(c);

        c *= 10;
        System.out.println(c);

        int d = 100;

        d++;
        System.out.println(d);

        d--;
        System.out.println(d);

    }

}
